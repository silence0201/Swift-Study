

//定义计算长方形面积函数
func rectangleArea(width: Double, height: Double) -> Double {
    let area = width * height
    return area
}

//定义计算三角形面积函数
func triangleArea(bottom: Double, height: Double) -> Double {
    let area = 0.5 * bottom * height
    return area
}

